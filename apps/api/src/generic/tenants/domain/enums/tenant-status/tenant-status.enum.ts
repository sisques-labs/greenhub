/**
 * TenantStatusEnum represents the status of a tenant.
 * ACTIVE: The tenant is active and operational.
 * INACTIVE: The tenant is inactive and not operational.
 * SUSPENDED: The tenant is suspended and cannot be used.
 */
export enum TenantStatusEnum {
	ACTIVE = 'ACTIVE',
	INACTIVE = 'INACTIVE',
	SUSPENDED = 'SUSPENDED',
}

