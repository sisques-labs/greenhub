/**
 * Represents the various possible units of measurement for length/dimensions.
 *
 * @remarks
 * This enum defines the different units of measurement available for dimensions.
 *
 * @enum
 * - {@link LengthUnitEnum.MILLIMETER}: Millimeter (mm)
 * - {@link LengthUnitEnum.CENTIMETER}: Centimeter (cm)
 * - {@link LengthUnitEnum.METER}: Meter (m)
 * - {@link LengthUnitEnum.INCH}: Inch (in)
 * - {@link LengthUnitEnum.FOOT}: Foot (ft)
 */
export enum LengthUnitEnum {
  /** Millimeter (mm) */
  MILLIMETER = 'MILLIMETER',

  /** Centimeter (cm) */
  CENTIMETER = 'CENTIMETER',

  /** Meter (m) */
  METER = 'METER',

  /** Inch (in) */
  INCH = 'INCH',

  /** Foot (ft) */
  FOOT = 'FOOT',
}
